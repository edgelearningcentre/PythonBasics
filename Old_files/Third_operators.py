""" Python acts as a simple calculator
"""
# Math operators from Highest to Lowest Precedence


# operator Exponent (**) , it means 2*2*2 (2 into 3 times)
a = 2 ** 3
print(a)


# operator Modulus/ Remainder (%) 22/8 

b = 22 % 8
print(b)

# operator Integer Division/ Floored Quotient (//)

c = 22 // 8
print(c)

# operator Division (/)

d = 22 / 8
print(d)


# operator Multiplication (*)

e = 3 * 5
print(e)

# operator Subtraction (-)

f = 5 - 2
print(f)

# operator Addition (+)
g = 2 + 2
print(g)

# example of an single expression

h = (5 - 1) * ((7 + 1) / (3 - 1))
print(h)

