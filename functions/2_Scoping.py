def addition():
    # scoping 
    num1 = int(input("Enter num 1 = "))
    num2 = int(input("Enter num 2 = "))
    print(f"Answer = {num1+num2}")
    print(num1)


def substraction():
    # scoping 
    num1 = int(input("Enter num 1 = "))
    num2 = int(input("Enter num 2 = "))
    print(f"Answer = {num1-num2}")
    print(num1)



addition()
substraction()


