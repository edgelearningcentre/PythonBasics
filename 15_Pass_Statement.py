# The Pass Statement can be used when a statement is 
# required syntactically but the program requires no action.
number = 21
while number > 1:
    pass     # requires no action just to pass from previous to next line for execution
    number = number - 1
    print(number)

# Pass can be used is as  a place holder for a function or conditional body
# when we are working on new code, allowing us to keep thinking at more abstract level. 
